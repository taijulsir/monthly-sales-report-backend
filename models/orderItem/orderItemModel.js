import mongoose from 'mongoose';

// Order Item Schema
const orderItemSchema = new mongoose.Schema({
    orderId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'Order',
        required: true
    },
    productId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'Product',
        required: true
    },
    unitPrice: {
        type: Number,
        required: true
    },
    quantity: {
        type: Number,
        required: true
    },
    total: {
        type: Number,
        required: true
    }  // unitPrice * quantity
});

// Models
const OrderItem = mongoose.model('OrderItem', orderItemSchema);

// Export models
export { OrderItem };
